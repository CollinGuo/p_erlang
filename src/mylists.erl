-module(mylists).

%% ====================================================================
%% API functions
%% ====================================================================
-export([map/2, sum/1]).


%% ====================================================================
%% Internal functions
%% ====================================================================


map(_, []) ->
    [];
map(F, [H | T]) ->
    [F(H) | map(F, T)].

sum([H | T]) ->
    H + sum(T);
sum([]) ->
    0.
